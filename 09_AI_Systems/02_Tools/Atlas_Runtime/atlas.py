    sub.add_parser("health-check", help="Verify the launch package is present, safe, and readable.")
    sub.add_parser("brief", help="Generate a daily CEO briefing.")
    sub.add_parser("score-leads", help="Score real leads in Lead_Tracker.csv.")
    sub.add_parser("dashboard", help="Regenerate docs/atlas-dashboard-data.js.")

    p_proposal = sub.add_parser("proposal", help="Generate a proposal draft.")
    p_proposal.add_argument("--client", default=None, help="Client name (optional).")

    sub.add_parser("outreach", help="Generate outreach drafts (never sends).")
    sub.add_parser("content-pack", help="Generate a daily content pack.")

    p_delivery = sub.add_parser("delivery-checklist", help="Generate a client delivery checklist.")
    p_delivery.add_argument("--client", default=None, help="Client name (optional).")

    sub.add_parser("payment-report", help="Report pipeline payment status.")
    sub.add_parser("war-room", help="Run brief + dashboard + payment-report + content-pack.")

    sub.add_parser("revenue-sprint", help="Generate the PRJ-009 revenue sprint status report.")

    p_ai_proposal = sub.add_parser("ai-agent-proposal", help="Generate an AI Agent Starter Pack proposal draft.")
    p_ai_proposal.add_argument("--client", default=None, help="Client name (optional).")

    p_ai_delivery = sub.add_parser("ai-agent-delivery", help="Generate the AI Agent Starter Pack delivery checklist.")
    p_ai_delivery.add_argument("--client", default=None, help="Client name (optional).")

    sub.add_parser("first-5-outreach", help="Generate the first-5 outreach execution pack (drafts only).")

    return parser


