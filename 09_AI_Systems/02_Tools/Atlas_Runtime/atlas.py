def cmd_health_check(cfg, args):
    print("Atlas Runtime - Health Check")
    print("=" * 60)
    ok = True

    print("\n[1] Required files/folders")
    for rel in cfg["health_check_required_paths"]:
        p = rp(rel)
        if p.exists():
            print(f"  [PASS] {rel}")
        else:
            print(f"  [FAIL] Missing: {rel}")
            ok = False

    print("\n[2] Approved payment link present in key files")
    link = cfg["payment_link"]
    for rel in cfg["health_check_payment_link_files"]:
        p = rp(rel)
        if not p.exists():
            print(f"  [WARN] File not found (skipped): {rel}")
            continue
        try:
            text = p.read_text(encoding="utf-8", errors="ignore")
        except Exception as exc:
            print(f"  [WARN] Could not read {rel}: {exc}")
            continue
        if link in text:
            print(f"  [PASS] Link present in {rel}")
        else:
            print(f"  [WARN] Link not found in {rel}")

    print("\n[3] Credential pattern scan (looking for actual key:value secrets, not warnings)")
    hits = scan_for_credentials(cfg)
    if hits:
        ok = False
        for h in hits:
            print(f"  [FAIL] Possible credential value in {h[0]} (line {h[1]}): {h[2]}")
    else:
        print("  [PASS] No credential-like values found.")

    print("\n[4] CSV trackers readable")
    for key in ("lead_tracker", "outreach_tracker", "client_pipeline", "content_calendar", "content_analytics"):
        p = rp(cfg["paths"][key])
        rows = read_csv_rows(p)
        if p.exists():
            print(f"  [PASS] {key}: {len(rows)} data row(s) read")
        else:
            print(f"  [FAIL] {key}: file missing")
            ok = False

    print("\n[5] Dashboard data file present")
    dash = rp(cfg["paths"]["dashboard_data_js"])
    if dash.exists():
        print(f"  [PASS] {cfg['paths']['dashboard_data_js']}")
    else:
        print(f"  [FAIL] Missing: {cfg['paths']['dashboard_data_js']}")
        ok = False

    print("\n" + "=" * 60)
    print("HEALTH CHECK RESULT:", "PASS" if ok else "FAIL")
    print("=" * 60)
    return 0 if ok else 1


def scan_for_credentials(cfg):
    """Return list of (relative_path, line_no, matched_text) for real credential-like values."""
    hits = []
    exts = set(cfg["credential_scan_extensions"])
    for rel_dir in cfg["credential_scan_dirs"]:
        base = rp(rel_dir)
        if not base.exists():
            continue
        for path in base.rglob("*"):
            if not path.is_file() or path.suffix.lower() not in exts:
                continue
            try:
                text = path.read_text(encoding="utf-8", errors="ignore")
            except Exception:
                continue
            for i, line in enumerate(text.splitlines(), start=1):
                m = CREDENTIAL_PATTERN.search(line)
                if m:
                    hits.append((str(path.relative_to(REPO_ROOT)), i, m.group(0)))
    return hits


