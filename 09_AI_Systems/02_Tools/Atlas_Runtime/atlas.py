def compute_metrics(cfg):
    paths = cfg["paths"]

    leads = real_rows(read_csv_rows(rp(paths["lead_tracker"])))
    outreach = real_rows(read_csv_rows(rp(paths["outreach_tracker"])))
    pipeline = real_rows(read_csv_rows(rp(paths["client_pipeline"])))
    content = real_rows(read_csv_rows(rp(paths["content_calendar"])))
    analytics = real_rows(read_csv_rows(rp(paths["content_analytics"])))

    leads_found = len(leads)
    leads_qualified = len([
        r for r in leads
        if (r.get("Lead Score", "").strip() not in ("", "0"))
        and r.get("Status", "").strip().lower() not in ("", "new")
    ])

    outreach_drafted = len(outreach)
    outreach_sent = len([r for r in outreach if r.get("Date Sent", "").strip()])
    outreach_approved = len([r for r in outreach if r.get("CEO Approved", "").strip().lower() == "yes"])

    pipeline_active = len(pipeline)
    payments_received = len([r for r in pipeline if r.get("Payment Status", "").strip().lower() == "paid"])
    payment_links_sent = len([r for r in pipeline if r.get("Payment Link Sent", "").strip().lower() == "yes"])
    revenue_collected = 0.0
    for r in pipeline:
        if r.get("Payment Status", "").strip().lower() == "paid":
            try:
                revenue_collected += float(str(r.get("Quoted Price USD", "0")).replace("$", "").strip() or 0)
            except ValueError:
                pass

    content_drafted = len(content)
    content_published = len([r for r in content if r.get("Status", "").strip().lower() == "posted"])

    total_views = 0
    for r in analytics:
        try:
            total_views += int(str(r.get("Views", "0")).strip() or 0)
        except ValueError:
            pass

    return {
        "leads_found": leads_found,
        "leads_qualified": leads_qualified,
        "outreach_drafted": outreach_drafted,
        "outreach_sent": outreach_sent,
        "outreach_approved": outreach_approved,
        "pipeline_active": pipeline_active,
        "payments_received": payments_received,
        "payment_links_sent": payment_links_sent,
        "revenue_collected": revenue_collected,
        "content_drafted": content_drafted,
        "content_published": content_published,
        "total_views": total_views,
        "raw": {
            "leads": leads, "outreach": outreach, "pipeline": pipeline,
            "content": content, "analytics": analytics,
        },
    }


